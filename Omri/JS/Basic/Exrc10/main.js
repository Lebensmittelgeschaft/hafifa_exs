function add(n1, n2) {
    document.getElementById("result").innerHTML = n1 + n2;
}

function sub(n1, n2) {
    document.getElementById("result").innerHTML = n1 - n2;
}

function mul(n1, n2) {
    document.getElementById("result").innerHTML = n1 * n2;
}

function div(n1, n2) {
    document.getElementById("result").innerHTML = n1 / n2;
}